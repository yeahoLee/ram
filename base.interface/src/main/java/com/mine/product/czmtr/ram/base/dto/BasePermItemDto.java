package com.mine.product.czmtr.ram.base.dto;

public class BasePermItemDto {
    private String id;
    private String sysMark;
    private String code;
    private String chsName;
    private String engName;
    private String menuPermName;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSysMark() {
        return sysMark;
    }

    public void setSysMark(String sysMark) {
        this.sysMark = sysMark;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getChsName() {
        return chsName;
    }

    public void setChsName(String chsName) {
        this.chsName = chsName;
    }

    public String getEngName() {
        return engName;
    }

    public void setEngName(String engName) {
        this.engName = engName;
    }

    public String getMenuPermName() {
        return menuPermName;
    }

    public void setMenuPermName(String menuPermName) {
        this.menuPermName = menuPermName;
    }
}
